# Hypotenuse of a right triangle
import math

a = float(input("Enter the value of a side A: "))
b = float(input("Enter the value of a side B: "))

c = math.sqrt(math.pow(a, 2) + math.pow(b, 2))

print(f"The hypotenuse of a triangle is: {round(c,2)}cm")
